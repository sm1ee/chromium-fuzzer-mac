#!/bin/bash
set -euo pipefail

OPS_ROOT="/Users/smlee/chromium-worker-ops"
# shellcheck source=/dev/null
source "$OPS_ROOT/config/worker.env"

if [ "$#" -eq 0 ]; then
    set -- webcodecs_video_decoder_fuzzer
fi

lock_dir="$DATA_ROOT/state/build.lockdir"
if ! /bin/mkdir "$lock_dir" 2>/dev/null; then
    echo "build already active: $lock_dir" >&2
    exit 75
fi
cleanup() { /bin/rmdir "$lock_dir" 2>/dev/null || true; }
trap cleanup EXIT INT TERM

if [ ! -d "$SRC_ROOT/.git" ] || [ ! -f "$EXPECTED_HEAD_FILE" ]; then
    echo "checkout is not attested; run bootstrap-checkout.sh first" >&2
    exit 2
fi
read -r expected_head _ < "$EXPECTED_HEAD_FILE"
head_value="$(/usr/bin/git -C "$SRC_ROOT" rev-parse HEAD)"
dirty_count="$(/usr/bin/git -C "$SRC_ROOT" status --porcelain --untracked-files=no | /usr/bin/wc -l | /usr/bin/tr -d ' ')"
if [ "$head_value" != "$expected_head" ] || [ "$dirty_count" != "0" ]; then
    echo "source gate failed head=$head_value expected=$expected_head dirty=$dirty_count" >&2
    exit 3
fi
if ! /usr/bin/xcrun metal --version >/dev/null 2>&1; then
    echo "Metal Toolchain is missing for $XCODE_APP" >&2
    exit 4
fi

gn_args='is_asan=true is_debug=false is_component_build=false symbol_level=1 target_cpu="arm64" use_libfuzzer=true use_sanitizer_coverage=true use_system_xcode=true use_autogenerated_modules=false v8_enable_verify_heap=true'
cd "$SRC_ROOT"
/usr/bin/caffeinate -dimsu "$DEPOT_TOOLS/gn" gen "$OUT_DIR" --args="$gn_args"
/usr/bin/caffeinate -dimsu "$DEPOT_TOOLS/autoninja" -j "$NINJA_JOBS" -C "$OUT_DIR" "$@"

for target in "$@"; do
    "$OPS_ROOT/bin/provenance-status.sh" "$target" >/dev/null
    echo "built target=$target binary=$OUT_DIR/$target"
done
